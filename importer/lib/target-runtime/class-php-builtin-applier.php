<?php
/**
 * Runtime applier for PHP's built-in development server.
 *
 * PHP's built-in server (php -S) doesn't read .user.ini files, but it
 * supports a router script that runs before every request. This applier writes:
 *
 * 1. {state_dir}/bootstrap.php — constants, server vars
 * 2. {docroot}/router.php      — router that loads the bootstrap, runs error
 *                                 handlers, serves static files, and falls
 *                                 through to WordPress for dynamic requests
 * 3. {state_dir}/start.sh      — shell script with the exact php -S command
 *
 * The developer just runs: bash {state_dir}/start.sh
 */
class PhpBuiltinApplier extends RuntimeApplier
{
    private string $host = 'localhost';
    private int $port = 8881;

    public function apply(RuntimeManifest $manifest, string $docroot, string $state_dir): array
    {
        $summary = [];

        // 1. Write bootstrap.php (constants + server vars only)
        $bootstrap_path = $state_dir . '/bootstrap.php';
        $bootstrap = $this->generate_bootstrap($manifest, $docroot);
        $this->write_file($bootstrap_path, $bootstrap);
        $summary[] = "Wrote {$bootstrap_path}";

        // 2. Write router.php in the docroot
        $router_path = $docroot . '/router.php';
        $router = $this->generate_router($manifest, $bootstrap_path);
        $this->write_file($router_path, $router);
        $summary[] = "Wrote {$router_path}";

        // 3. Write start.sh
        $start_path = $state_dir . '/start.sh';
        $start_script = $this->generate_start_script($manifest, $docroot, $router_path);
        $this->write_file($start_path, $start_script);
        chmod($start_path, 0755);
        $summary[] = "Wrote {$start_path}";

        $summary[] = '';
        $summary[] = 'Start the server:';
        $summary[] = "  bash {$start_path}";
        $summary[] = '';
        $summary[] = "Then open http://{$this->host}:{$this->port} in your browser.";

        return $summary;
    }

    /**
     * The router script runs on every request. It loads the bootstrap (which
     * sets constants and server vars), runs error handlers for missing files
     * (like thumbnail generation), then serves static files directly or falls
     * through to WordPress.
     */
    private function generate_router(RuntimeManifest $manifest, string $bootstrap_path): string
    {
        $escaped_bootstrap = addslashes($bootstrap_path);

        $parts = [];
        $parts[] = '<?php';
        $parts[] = '/**';
        $parts[] = ' * Router script for PHP\'s built-in development server.';
        $parts[] = ' * Generated by apply-runtime — do not edit.';
        $parts[] = ' *';
        $parts[] = ' * Loads the runtime bootstrap (constants, server vars), runs';
        $parts[] = ' * error handlers for missing files, then either serves static';
        $parts[] = ' * files or routes dynamic requests to WordPress.';
        $parts[] = ' */';
        $parts[] = '';
        $parts[] = "// Load the runtime bootstrap — defines constants and sets server vars.";
        $parts[] = "require '{$escaped_bootstrap}';";
        $parts[] = '';

        // Inline error handlers into the router. The thumbnail handler checks
        // the request URI and exits if it generates a thumbnail, so it must
        // run before the static-file and WordPress routing below.
        if ($this->has_thumbnail_handler($manifest)) {
            $parts[] = $this->generate_thumbnail_handler();
            $parts[] = '';
        }

        $parts[] = '$uri = $_SERVER[\'REQUEST_URI\'] ?? \'/\';';
        $parts[] = '$path = parse_url($uri, PHP_URL_PATH);';
        $parts[] = '';
        $parts[] = '// Map the URL to a file in the docroot.';
        $parts[] = '$file = $_SERVER[\'DOCUMENT_ROOT\'] . $path;';
        $parts[] = '';
        $parts[] = '// Serve existing static files directly (CSS, JS, images, etc.).';
        $parts[] = '// Return false to let PHP\'s built-in server use its default static handler.';
        $parts[] = 'if ($path !== \'/\' && file_exists($file) && is_file($file)) {';
        $parts[] = '    return false;';
        $parts[] = '}';
        $parts[] = '';
        $parts[] = '// For directory requests, look for index.php or index.html.';
        $parts[] = 'if (is_dir($file)) {';
        $parts[] = '    if (file_exists($file . \'/index.php\')) {';
        $parts[] = '        $_SERVER[\'SCRIPT_NAME\'] = $path . \'/index.php\';';
        $parts[] = '        require $file . \'/index.php\';';
        $parts[] = '        return;';
        $parts[] = '    }';
        $parts[] = '    if (file_exists($file . \'/index.html\')) {';
        $parts[] = '        return false;';
        $parts[] = '    }';
        $parts[] = '}';
        $parts[] = '';
        $parts[] = '// Everything else goes to WordPress via index.php (pretty permalinks).';
        $parts[] = '$_SERVER[\'SCRIPT_NAME\'] = \'/index.php\';';
        $parts[] = 'require $_SERVER[\'DOCUMENT_ROOT\'] . \'/index.php\';';

        return implode("\n", $parts) . "\n";
    }

    /**
     * Generate a shell script that starts the built-in server with the right
     * arguments, including any INI overrides from the manifest.
     */
    private function generate_start_script(RuntimeManifest $manifest, string $docroot, string $router_path): string
    {
        $lines = [];
        $lines[] = '#!/usr/bin/env bash';
        $lines[] = '# Start the PHP built-in server for the imported site.';
        $lines[] = '# Generated by apply-runtime — do not edit.';
        $lines[] = '#';
        $lines[] = '# Source host: ' . $manifest->source;
        $lines[] = '';
        $lines[] = 'set -euo pipefail';
        $lines[] = '';

        // Build the php command with INI overrides
        $php_args = [];
        $php_args[] = 'php';

        foreach ($manifest->php_ini as $key => $value) {
            $php_args[] = "-d {$key}={$value}";
        }

        $php_args[] = '-S ' . $this->host . ':' . $this->port;
        $php_args[] = '-t ' . escapeshellarg($docroot);
        $php_args[] = escapeshellarg($router_path);

        $lines[] = 'echo "Starting PHP built-in server..."';
        $lines[] = 'echo "  http://' . $this->host . ':' . $this->port . '"';
        $lines[] = 'echo ""';
        $lines[] = '';
        $lines[] = implode(" \\\n    ", $php_args);
        $lines[] = '';

        return implode("\n", $lines);
    }
}
